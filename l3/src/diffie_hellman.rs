    // Binary field for EC tests (GF(2^4) = GF16)
    #[derive(Clone, Debug)]
    struct F2_4;
    static F2_4_IRRED: [BigInt<4>; 5] = [
        BigInt::from_u64(1), // x^0
        BigInt::from_u64(1), // x^1
        BigInt::from_u64(0), // x^2
        BigInt::from_u64(0), // x^3
        BigInt::from_u64(1), // x^4  => x^4 + x + 1
    ];
    impl FieldConfig<4> for F2_4 {
        fn modulus() -> &'static BigInt<4> {
            &F2_MOD
        }
        fn irreducible() -> &'static [BigInt<4>] {
            &F2_4_IRRED
        }
    }
    type GF16 = BinaryField<F2_4, 4, 4>;

    #[test]
    fn test_ecdh_binary_curve_agreement() {
        use crate::elliptic_curve::BinaryEllipticCurve;

        // Binary curve: y² + xy = x³ + ax² + b over GF(2^4)
        let a = GF16::from_u64(1);
        let b = GF16::from_u64(1);
        let curve = BinaryEllipticCurve::new(a, b);

        // Find an on-curve generator point with x != 0 (to avoid small order points)
        let mut g_opt = None;
        for x in 1..16u64 {
            for y in 0..16u64 {
                let pt = Point::Affine {
                    x: GF16::from_u64(x),
                    y: GF16::from_u64(y),
                };
                if curve.is_on_curve(&pt) {
                    g_opt = Some(pt);
                    break;
                }
            }
            if g_opt.is_some() {
                break;
            }
        }
        let generator = g_opt.expect("No generator point found on binary curve");

        let params = DHParamsBinaryEC {
            field_description: "GF(2^4)".to_string(),
            curve: curve.clone(),
            generator: generator.clone(),
            q: BigInt::from_u64(16), // Approximate, sufficient for testing
        };

        // Use different scalars that will produce different public keys
        let alice_sk = BigInt::from_u64(2);
        let alice_pk = DHBinaryEC::<GF16, 4>::compute_public_key(&params, &alice_sk);

        let bob_sk = BigInt::from_u64(3);
        let bob_pk = DHBinaryEC::<GF16, 4>::compute_public_key(&params, &bob_sk);

        // Both compute shared secret
        let alice_ss = DHBinaryEC::<GF16, 4>::compute_shared_secret(&params, &alice_sk, &bob_pk);
        let bob_ss = DHBinaryEC::<GF16, 4>::compute_shared_secret(&params, &bob_sk, &alice_pk);

        // Core ECDH property: shared secrets must match
        assert_eq!(alice_ss, bob_ss);

        // Shared secret should be on the curve
        assert!(params.curve.is_on_curve(&alice_ss));

        // Public keys should be on the curve
        assert!(params.curve.is_on_curve(&alice_pk));
        assert!(params.curve.is_on_curve(&bob_pk));
    }

